import Foundation
import UIKit
extension UIColor{
    static func AppThemeDrakBlueColor() -> UIColor{
        return #colorLiteral(red: 0.1051146612, green: 0.3041367531, blue: 0.5033302307, alpha: 1)
    }
    
    static func AppThemeGreenColor() -> UIColor {
        return #colorLiteral(red: 0.137254902, green: 0.737254902, blue: 0.7098039216, alpha: 1)
    }
    static func AppThemeSkyBlueColor() -> UIColor {
        return #colorLiteral(red: 0.3019607843, green: 0.662745098, blue: 0.8156862745, alpha: 1)
    }
    
    
}


